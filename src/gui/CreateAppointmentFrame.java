    private void loadDoctorid() {
     try
     {
         List<String> list=AppointmentDAO.getDoctorId();
         for(String did:list)
         {
             jcDoctorId.addItem(did);
         }
     }catch(SQLException se){
           JOptionPane.showMessageDialog(null, "Error in Loading the data from the database", "Error", JOptionPane.ERROR);
           se.printStackTrace();
       }
    }

    private boolean validateInputs() {
     if(jcDoctorId.getSelectedItem().toString().isEmpty() || txtPatientId.getText().isEmpty() || jcDate.getDate().toString().isEmpty() || txtTime.getText().isEmpty() || txtCharge.getText().isEmpty())
     {
         return false;
     }
     return true;
    }

    private void clearText() {
   txtPatientId.setText("");
   txtTime.setText("");
   txtTime.setText("");
   txtCharge.setText("");
    }

