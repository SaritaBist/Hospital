    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
      boolean result=validateInputs();
      if(!result)
      {
           JOptionPane.showMessageDialog(null,"Please Fill All the field", "Information",JOptionPane.INFORMATION_MESSAGE);
            return; 
      }
      try
      {
          AppointmentPojo p=new AppointmentPojo();
          p.setDoctorid(jcDoctorId.getSelectedItem().toString());
          p.setPatientid(txtPatientId.getText());
          Date d=new Date(jcDate.getDate().getTime());
          p.setDate(d);
          p.setTime(txtTime.getText());
          p.setCharge(Double.parseDouble(txtCharge.getText()));
          
          boolean Status=AppointmentDAO.createAppointment(p);
          if(Status)
          {
              JOptionPane.showMessageDialog(null,"Appointment Created Successfully","success",JOptionPane.INFORMATION_MESSAGE);
                 clearText();
                 return;
          }
          else
            {
                JOptionPane.showMessageDialog(null,"Appointment is not created","error",JOptionPane.ERROR_MESSAGE); 
                return;
                
            }
          
      }catch(SQLException ex)
        { 
            JOptionPane.showMessageDialog(null,"Error in Data Base","error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
      
        
    }//GEN-LAST:event_btnCreateActionPerformed

