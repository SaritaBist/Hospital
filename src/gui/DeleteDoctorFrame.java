    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
       try
       {
           String deptid=cbDoctorId.getSelectedItem().toString();
           boolean result=DoctorDAO.deleteDoctor(deptid);
           if(result)
           {
                JOptionPane.showMessageDialog(null,"Data deleted Successfully","success",JOptionPane.INFORMATION_MESSAGE);
                return;
           }
            else
            {
                JOptionPane.showMessageDialog(null,"Data not delete","error",JOptionPane.ERROR_MESSAGE); 
                return;
            }
       }
         catch(SQLException ex)
        { 
            JOptionPane.showMessageDialog(null,"Error in Data Base","error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

