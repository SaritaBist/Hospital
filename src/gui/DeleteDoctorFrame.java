    private void getAllDoctorId() {
        try
        {
           List<String> list=DoctorDAO.generateDoctorId();
           for(String DoctorId:list)
           {
               cbDoctorId.addItem(DoctorId);
           }
            
        }catch(SQLException se){
           JOptionPane.showMessageDialog(null, "Error in Loading the data from the database", "Error", JOptionPane.ERROR);
           se.printStackTrace();
       }
    }
