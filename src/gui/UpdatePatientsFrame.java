    private void btnUpdatePatientsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePatientsActionPerformed
        
           boolean result=validateInputs();    
          if(!result)
          {
               JOptionPane.showMessageDialog(null,"please fill all the fiels","error",JOptionPane.ERROR_MESSAGE);
            return;
          }
          try
          {
               PatientPojo p=new PatientPojo();
            
            p.setPatientId(jcPatientId.getSelectedItem().toString());
            p.setPatientName(txtPatientName.getText());
            p.setAge( Integer.parseInt(txtAge.getText()));
            p.setGender(jcGender.getSelectedItem().toString());
            p.setBloodgroup(jcBloodgrp.getSelectedItem().toString());
            p.setContactNo( Long.parseLong(txtContactNo.getText()));
            p.setBedNo(txtBedNo.getText());
            p.setAddress(txtAddress.getText());
            
            boolean status=PatientDAO.updatePatients(p);
            if(!status)
            {
                JOptionPane.showMessageDialog(null," Patients not Updated","error",JOptionPane.ERROR_MESSAGE);
                return;
                
            }
            JOptionPane.showMessageDialog(null,"Patients Updated successfully","error",JOptionPane.INFORMATION_MESSAGE);
            clearText();
            
            
            
              
          } catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB", "error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException nfex)
        {
            JOptionPane.showMessageDialog(null,"please enter numeric digits", "error",JOptionPane.ERROR_MESSAGE);
            nfex.printStackTrace();
        }
        
               

    }//GEN-LAST:event_btnUpdatePatientsActionPerformed

    private void jcPatientIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcPatientIdActionPerformed

        try
        {
            String id=jcPatientId.getSelectedItem().toString();
            PatientPojo p=PatientDAO.getAllPatientsById(id);
            jcPatientId.setSelectedItem(p.getPatientId());
            txtPatientName.setText(p.getPatientName());
            txtAge.setText( String.valueOf(p.getAge()));
            jcGender.setSelectedItem(p.getGender());
            jcBloodgrp.setSelectedItem(p.getBloodgroup());
            txtContactNo.setText( String.valueOf(p.getContactNo()));
            txtBedNo.setText(p.getBedNo());
            txtAddress.setText(p.getAddress());

        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in Data Base","error",JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jcPatientIdActionPerformed

