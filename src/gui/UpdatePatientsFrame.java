    private void loadPatientsId() {
      try
     {    jcPatientId.removeAllItems();
         List<String> pid=PatientDAO.getAllPatienstid();
         for(String id:pid)
         {
             jcPatientId.addItem(id);
         }
        
         
     }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB", "error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

