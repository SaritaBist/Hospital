    private void getAllPatientsDetails() {
        
       try
       {
           List<PatientPojo> patientsList=PatientDAO.getAllPatients();
           DefaultTableModel dm=(DefaultTableModel)jtResult.getModel();
           Object rows []=new Object [8];
           for(PatientPojo p:patientsList)
           {
               rows[0]=p.getPatientId();
               rows[1]=p.getPatientName();
               rows[2]=p.getAge();
               rows[3]=p.getGender();
               rows[4]=p.getBloodgroup();
               rows[5]=p.getBedNo();
               rows[6]=p.getContactNo();
               rows[7]=p.getAddress();
                dm.addRow(rows);
           }
           
       }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error In data Bse", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }
